package poo_base_exercices;



public class MathOps {
	private double x;
	private double y;
	
	public double addition() {
		return x + y;
	}
	
	/**
	 * @return
	 */
	public double substract() {
		return x - y;
	}
	
	/**
	 * @return
	 */
	public double multiply() {
		return x * y;
	}
	
	public double divide() {
		return x / y;
	}
}
